package practice;

public class pattern2 {

	public static void main(String[] args) {
		
				int i, j, row=6;   
				 
				for(i=1; i<=row; i++)   
				{   
				      for(j=1; j<=i; j++)   
				        {   
				            System.out.print(j);   
				         }   
				 	System.out.println();   
				} 
				
				for(i=1; i<=row; i++)   
				{   
				      for(j=6; j>=i; j--)   
				        {   
				            System.out.print(j);   
				         }   
				 	System.out.println();   
				} 
			}   
		}  
